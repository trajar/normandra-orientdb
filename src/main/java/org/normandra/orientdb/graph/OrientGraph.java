public class OrientGraph extends OrientDatabaseSession implements Graph {
    private static final Logger logger = LoggerFactory.getLogger(OrientGraph.class);

    private final GraphMeta meta;

    private final com.tinkerpop.blueprints.impls.orient.OrientGraph graph;

    public OrientGraph(final GraphMeta meta, final com.tinkerpop.blueprints.impls.orient.OrientGraph graph, final Map<String, OrientQuery> statements, final EntityCache cache) {
        super(graph.getRawGraph(), statements, cache);
        this.meta = meta;
        this.graph = graph;
    }

    public final GraphMeta getMeta() {
        return this.meta;
    }

    public final com.tinkerpop.blueprints.impls.orient.OrientGraph graph() {
        return this.graph;
    }

    @Override
    public OrientNode addNode(final EntityMeta meta, final Object instance) throws NormandraException {
        if (null == meta || null == instance) {
            return null;
        }

        // get entity context
        final Object key = meta.getId().fromEntity(instance);
        if (!this.meta.isNodeEntity(meta)) {
            throw new IllegalArgumentException("Entity [" + meta + "] is not a registered node type.");
        }

        final OrientVertex vertex;
        try (final Transaction tx = this.beginTransaction()) {
            // ensure we don't already have node
            if (this.exists(meta, key)) {
                throw new NormandraException("Node already exists.");
            }

            // save entity using property model
            final String schemaName = meta.getTable();
            final String clusterName = null;
            vertex = this.graph.addVertex(schemaName, clusterName);
            if (null == vertex) {
                return null;
            }
            final PropertyModel model = this.buildModel(meta, vertex);
            final GraphDataHandler handler = new GraphDataHandler(model);
            new EntityPersistence(this).save(meta, instance, handler);
            tx.success();
        } catch (final Exception e) {
            throw new NormandraException("Unable to add node [" + instance + "].", e);
        }

        final EntityReference reference = new StaticEntityReference<>(instance);
        final OrientNode node = this.buildNode(meta, key, vertex, reference);
        return node;
    }

    @Override
    public Collection<Node> getNodes(final EntityMeta meta, final Iterable<?> keys) throws NormandraException {
        if (null == meta || null == keys) {
            return null;
        }

        // check cache
        final Set<Object> keyset = new HashSet<>();
        for (final Object obj : keys) {
            keyset.add(obj);
        }
        if (keyset.isEmpty()) {
            return Collections.emptyList();
        }
        final Map<Object, OrientNode> map = new HashMap<>(keyset.size());
        map.putAll(this.cache.find(meta, keyset, OrientNode.class));
        keyset.removeAll(map.keySet());
        if (keyset.isEmpty()) {
            return Collections.unmodifiableCollection(map.values());
        }

        // query documents
        final List<OrientNode> nodes = new ArrayList<>(map.values());
        try (final Transaction tx = this.beginTransaction()) {
            for (final Object key : keyset) {
                final OIdentifiable document = this.findIdByKey(meta, key);
                if (document != null) {
                    final OrientVertex vertex = new OrientVertex(this.graph, document);
                    final EntityReference reference = new OrientEntityReference<>(this, meta, document);
                    final OrientNode node = this.buildNode(meta, key, vertex, reference);
                    if (node != null) {
                        nodes.add(node);
                    }
                }
            }
            return Collections.unmodifiableCollection(nodes);
        } catch (final Exception e) {
            throw new NormandraException("Unable to get node entity [" + meta + "] by keys " + keyset + ".", e);
        }
    }

    @Override
    public OrientNode getNode(final EntityMeta meta, final Object key) throws NormandraException {
        if (null == meta || null == key) {
            return null;
        }

        // check cache
        final OrientNode cached = this.cache.get(meta, key, OrientNode.class);
        if (cached != null) {
            return cached;
        }

        // query document
        try (final Transaction tx = this.beginTransaction()) {
            final OIdentifiable document = this.findIdByKey(meta, key);
            if (document != null) {
                final OrientVertex vertex = new OrientVertex(this.graph, document);
                final EntityReference reference = new OrientEntityReference<>(this, meta, document);
                final OrientNode node = this.buildNode(meta, key, vertex, reference);
                if (node != null) {
                    return node;
                }
            }
        } catch (final Exception e) {
            throw new NormandraException("Unable to get node entity [" + meta + "] by key [" + key + "].", e);
        }

        // not found
        return null;
    }

    @Override
    public OrientEdge getEdge(final EntityMeta meta, final Object key) throws NormandraException {
        // check cache
        final OrientEdge cached = this.cache.get(meta, key, OrientEdge.class);
        if (cached != null) {
            return cached;
        }

        // query document
        try (final Transaction tx = this.beginTransaction()) {
            // query database
            final OIdentifiable document = this.findIdByKey(meta, key);
            final com.tinkerpop.blueprints.impls.orient.OrientEdge orientEdge = document != null ? this.graph.getEdge(document) : null;
            if (orientEdge != null) {
                final EntityReference reference = new OrientEntityReference<>(this, meta, document);
                final OrientEdge edge = this.buildEdge(meta, key, orientEdge, reference);
                if (edge != null) {
                    return edge;
                }
            }
        } catch (final Exception e) {
            throw new NormandraException("Unable to get node entity [" + meta + "] by key [" + key + "].", e);
        }

        // not found
        return null;
    }

    @Override
    public NodeQuery queryNodes(EntityMeta meta, String query, Map<String, Object> parameters) throws NormandraException {
        return this.queryVertices(query, parameters);
    }

    @Override
    public EdgeQuery queryEdges(EntityMeta meta, String query, Map<String, Object> parameters) throws NormandraException {
        return this.queryEdges(query, parameters);
    }

    public <T> OrientEdge<T> getEdge(final Class<T> clazz, final Object key) throws NormandraException {
        if (null == clazz || null == key) {
            return null;
        }

        final EntityMeta meta = this.meta.getEdgeMeta(clazz);
        if (null == meta) {
            return null;
        }

        return getEdge(meta, key);
    }

    public <T> Collection<Edge> getEdges(final Class<T> clazz, final Iterable<?> keys) throws NormandraException {
        if (null == clazz || null == keys) {
            return null;
        }

        // check meta context
        final EntityMeta meta = this.meta.getEdgeMeta(clazz);
        if (null == meta) {
            return null;
        }

        // check cache
        final Set<Object> keyset = new HashSet<>();
        for (final Object obj : keys) {
            keyset.add(obj);
        }
        if (keyset.isEmpty()) {
            return Collections.emptyList();
        }
        final Map<Object, OrientEdge> map = new HashMap<>(keyset.size());
        map.putAll(this.cache.find(meta, keyset, OrientEdge.class));
        keyset.removeAll(map.keySet());
        if (keyset.isEmpty()) {
            return Collections.unmodifiableCollection(map.values());
        }

        // query documents
        final List<OrientEdge> edges = new ArrayList<>(map.values());
        try (final Transaction tx = this.beginTransaction()) {
            for (final Object key : keyset) {
                final OIdentifiable document = this.findIdByKey(meta, key);
                if (document != null) {
                    final com.tinkerpop.blueprints.impls.orient.OrientEdge edge = this.graph.getEdge(document);
                    if (edge != null) {
                        final EntityReference<T> reference = new OrientEntityReference<>(this, meta, document);
                        final OrientEdge orientEdge = this.buildEdge(meta, key, edge, reference);
                        if (orientEdge != null) {
                            edges.add(orientEdge);
                        }
                    }
                }
            }
            return Collections.unmodifiableCollection(edges);
        } catch (final Exception e) {
            throw new NormandraException("Unable to get node entity [" + meta + "] by keys " + keyset + ".", e);
        }
    }

    public <E> OrientNodeQuery<E> queryVertices(final String sql) {
        return new OrientNodeQuery(this, new OrientSelfClosingQuery(this.database, sql));
    }

    public <E> OrientNodeQuery<E> queryVertices(final String sql, final Map<String, Object> params) {
        return new OrientNodeQuery(this, new OrientSelfClosingQuery(this.database, sql, params));
    }

    public <E> OrientNodeQuery<E> queryVertices(final String sql, final Collection<?> params) {
        return new OrientNodeQuery(this, new OrientSelfClosingQuery(this.database, sql, params));
    }

    public <E> OrientEdgeQuery<E> queryEdges(final String sql) {
        return this.queryEdges(sql, Collections.emptyList());
    }

    public <E> OrientEdgeQuery<E> queryEdges(final String sql, final Map<String, Object> params) {
        return new OrientEdgeQuery(this, new OrientSelfClosingQuery(this.database, sql, params));
    }

    public <E> OrientEdgeQuery<E> queryEdges(final String sql, final Collection<?> params) {
        return new OrientEdgeQuery(this, new OrientSelfClosingQuery(this.database, sql, params));
    }

    @Override
    public void close() {
        this.graph.shutdown(true, false);
        super.close();
    }

    final DataHolderFactory buildDataFactory() {
        return new OrientDataFactory(this);
    }

    final PropertyModel buildModel(final EntityMeta meta, final OrientElement document) {
        final PropertyFilter filter = this.meta.getPropertyFilter(meta);
        if (filter != null) {
            return new OrientPropertyModel(meta, document, filter);
        } else {
            return new OrientPropertyModel(meta, document);
        }
    }

    final EntityMeta findNodeMetaBySchema(final Optional<OClass> optionalType) {
        if (null == optionalType || !optionalType.isPresent()) {
            return null;
        }

        final OClass type = optionalType.get();
        if (null == type) {
            return null;
        }
        for (final String name : Arrays.asList(type.getName(), type.getShortName())) {
            final EntityMeta ctx = this.meta.getNodeMeta(name);
            if (ctx != null) {
                return ctx;
            }
        }
        return null;
    }

    final EntityMeta findEdgeMetaBySchema(final Optional<OClass> optionalType) {
        if (null == optionalType || !optionalType.isPresent()) {
            return null;
        }

        final OClass type = optionalType.get();
        if (null == type) {
            return null;
        }
        for (final String name : Arrays.asList(type.getName(), type.getShortName())) {
            final EntityMeta ctx = this.meta.getEdgeMeta(name);
            if (ctx != null) {
                return ctx;
            }
        }
        return null;
    }

    final <T> OrientNode<T> buildNode(final OResult result) {
        return buildNode(null, result);
    }

    final <T> OrientNode<T> buildNode(EntityMeta meta, final OResult result) {
        if (null == result) {
            return null;
        }

        if (result.isElement()) {
            final OElement element = result.getElement().get();
            if (element instanceof OVertex) {
                final OVertex vertex = (OVertex) element;
                if (null == meta) {
                    meta = findNodeMetaBySchema(element.getSchemaType());
                }
                final ORecord record = vertex.getRecord();
                final ODocument document = this.graph.getRawGraph().load(record);
                final Object key = OrientUtils.unpackKey(meta, document);
                return buildNode(meta, key, this.graph.getVertex(document), new OrientEntityReference<>(this, meta, record.getIdentity()));
            }
        }

        if (result.isRecord()) {
            final ORecord record = result.getRecord().get();
            final ODocument document = this.graph.getRawGraph().load(record);
            if (null == meta) {
                meta = findNodeMetaBySchema(document.getSchemaType());
            }
            return buildNode(meta, document, new OrientEntityReference<>(this, meta, record.getIdentity()));
        }

        throw new IllegalStateException();
    }

    final <T> OrientNode<T> buildNode(final EntityMeta meta, final ODocument document, final EntityReference<T> data) {
        if (null == document) {
            return null;
        }

        final Object key = OrientUtils.unpackKey(meta, document);
        return this.buildNode(meta, key, new com.tinkerpop.blueprints.impls.orient.OrientVertex(this.graph, document), data);
    }

    final <T> OrientNode<T> buildNode(final EntityMeta meta, final Object key, final com.tinkerpop.blueprints.impls.orient.OrientVertex vertex, final EntityReference<T> data) {
        if (null == meta || null == vertex || null == data) {
            return null;
        }

        final OrientNode cached = this.cache.get(meta, key, OrientNode.class);
        if (cached != null) {
            return cached;
        }

        final OrientNode<T> orientNode = new OrientNode<>(this, vertex, meta, data);
        this.cache.put(meta, key, orientNode);
        return orientNode;
    }

    final <T> OrientEdge<T> buildEdge(final OResult result) {
        return buildEdge(null, result);
    }

    final <T> OrientEdge<T> buildEdge(EntityMeta meta, final OResult result) {
        if (null == result) {
            return null;
        }

        if (result.isElement()) {
            final OElement element = result.getElement().get();
            if (null == meta) {
                meta = findEdgeMetaBySchema(element.getSchemaType());
            }
            if (element instanceof OEdge) {
                final OEdge edge = (OEdge) element;
                final ORecord record = edge.getRecord();
                final ODocument document = this.graph.getRawGraph().load(record);
                final Object key = OrientUtils.unpackKey(meta, document);
                return buildEdge(meta, key, this.graph.getEdge(document), new OrientEntityReference<>(this, meta, record.getIdentity()));
            }
        }

        if (result.isRecord()) {
            final ORecord record = result.getRecord().get();
            final ODocument document = this.graph.getRawGraph().load(record);
            if (null == meta) {
                meta = findNodeMetaBySchema(document.getSchemaType());
            }
            final Object key = OrientUtils.unpackKey(meta, document);
            return buildEdge(meta, key, this.graph.getEdge(document), new OrientEntityReference<>(this, meta, record.getIdentity()));
        }

        throw new IllegalStateException();
    }

    final <T> OrientEdge<T> buildEdge(final EntityMeta meta, final Object key, final com.tinkerpop.blueprints.impls.orient.OrientEdge edge, final EntityReference<T> data) {
        if (null == meta || null == edge || null == data) {
            return null;
        }

        final OrientEdge cached = this.cache.get(meta, key, OrientEdge.class);
        if (cached != null) {
            return cached;
        }

        final OrientEdge<T> orientEdge = new OrientEdge<>(this, edge, meta, data);
        this.cache.put(meta, key, orientEdge);
        return orientEdge;
    }
}
