package org.normandra.orientdb.graph;

import com.tinkerpop.blueprints.Direction;
import org.apache.commons.lang.NullArgumentException;
import org.apache.commons.lang.StringUtils;
import org.normandra.NormandraException;
import org.normandra.Transaction;
import org.normandra.data.EntityReference;
import org.normandra.data.GraphDataHandler;
import org.normandra.data.StaticEntityReference;
import org.normandra.graph.Edge;
import org.normandra.graph.Node;
import org.normandra.meta.EntityMeta;
import org.normandra.orientdb.data.OrientUtils;
import org.normandra.property.PropertyModel;
import org.normandra.util.ArraySet;
import org.normandra.util.EntityPersistence;

import java.util.Collections;
import java.util.HashSet;
import java.util.List;
import java.util.Set;
import java.util.stream.Collectors;

/**
 * an orient db graph node
 * <p>
 * Date: 12/21/14
 */
public class OrientNode<T> implements Node<T> {
    private final OrientGraph graph;

    private final EntityMeta meta;

    private final com.tinkerpop.blueprints.impls.orient.OrientVertex vertex;

    private EntityReference<T> data;

    public OrientNode(
            final OrientGraph graph,
            final com.tinkerpop.blueprints.impls.orient.OrientVertex vertex,
            final EntityMeta meta,
            final EntityReference<T> ref) {
        if (null == graph) {
            throw new NullArgumentException("graph");
        }
        if (null == vertex) {
            throw new NullArgumentException("vertex");
        }
        if (null == ref) {
            throw new NullArgumentException("reference");
        }
        this.graph = graph;
        this.vertex = vertex;
        this.meta = meta;
        this.data = ref;
    }

    public com.tinkerpop.blueprints.impls.orient.OrientVertex element() {
        return this.vertex;
    }

    @Override
    public void delete() throws NormandraException {
        try (final Transaction tx = this.graph.beginTransaction()) {
            this.vertex.remove();
            tx.success();
        } catch (final Exception e) {
            throw new NormandraException("Unable to remove vertex [" + this.vertex + "].", e);
        }

        final Object key = OrientUtils.unpackKey(this.meta, this.vertex.getRecord());
        this.graph.cache().remove(this.meta, key);
    }

    @Override
    public <E, N> boolean hasEdge(final Node<N> node, final E entity) throws NormandraException {
        if (null == node) {
            throw new NullArgumentException("node");
        }
        if (!(node instanceof OrientNode)) {
            throw new IllegalArgumentException("Node not of type orientdb - found [" + node + "].");
        }
        if (null == entity) {
            return false;
        }

        final EntityMeta meta = this.graph.getMeta().getEdgeMeta(entity.getClass());
        if (null == meta) {
            return false;
        }

        final Object key = meta.getId().fromEntity(entity);
        return this.graph.exists(meta, key);
    }

    @Override
    public <E, N> Edge<E> createEdge(final Node<N> node, final E entity) throws NormandraException {
        if (null == node) {
            throw new NullArgumentException("node");
        }
        if (!(node instanceof OrientNode)) {
            throw new IllegalArgumentException("Node not of type orientdb - found [" + node + "].");
        }
        if (null == entity) {
            return null;
        }

        final Object key = meta.getId().fromEntity(entity);
        final EntityMeta meta = this.graph.getMeta().getEdgeMeta(entity.getClass());
        if (null == meta) {
            return null;
        }

        final com.tinkerpop.blueprints.impls.orient.OrientEdge edge;
        try (final Transaction tx = this.graph.beginTransaction()) {
            final String schemaName = meta.getTable();
            final com.tinkerpop.blueprints.impls.orient.OrientVertex otherNode = ((OrientNode) node).element();
            edge = this.vertex.addEdge(schemaName, otherNode, schemaName);
            if (null == edge) {
                return null;
            }

            final PropertyModel model = this.graph.buildModel(meta, edge);
            final GraphDataHandler handler = new GraphDataHandler(model);
            new EntityPersistence(this.graph).save(meta, entity, handler);
            tx.success();
        } catch (final Exception e) {
            throw new NormandraException("Unable to add edge [" + entity + "].", e);
        }

        final EntityReference<E> reference = new StaticEntityReference<>(entity);
        return this.graph.buildEdge(meta, key, edge, reference);
    }

    @Override
    public int getDegree() throws NormandraException {
        final long count;
        try (final Transaction tx = this.graph.beginTransaction()) {
            count = this.vertex.countEdges(Direction.BOTH);
        } catch (final Exception e) {
            throw new NormandraException("Unable to get node degree from [" + this + "].", e);
        }

        if (count <= 0) {
            return 0;
        } else if (count >= Integer.MAX_VALUE) {
            throw new IllegalStateException("Edge count [" + count + "] is greater than maximum integer.");
        } else {
            return (int) count;
        }
    }

    @Override
    public Iterable<Node> getNeighbors() throws NormandraException {
        try (final Transaction tx = this.graph.beginTransaction()) {
            final Set<Node> neighbors = new ArraySet<>();
            for (final Node<?> node : this.graph.queryVertices("select expand(both()) from " + this.element().getId())) {
                if (!this.equals(node)) {
                    neighbors.add(node);
                }
            }
            return Collections.unmodifiableCollection(neighbors);
        } catch (final Exception e) {
            throw new NormandraException("Unable to get edges for [" + this + "].", e);
        }
    }

    @Override
    public Iterable<Node> getNeighbors(final Class edgeType, final Class nodeType) throws NormandraException {
        final EntityMeta edgeMeta = this.graph.getMeta().getEdgeMeta(edgeType);
        final EntityMeta nodeMeta = this.graph.getMeta().getNodeMeta(nodeType);

        try (final Transaction tx = this.graph.beginTransaction()) {
            final String label = edgeMeta != null ? "'" + edgeMeta.getTable() + "'" : "";
            final String query;
            if (nodeMeta != null) {
                final String type = "'" + nodeMeta.getTable() + "'";
                query = "select expand(unionall(outE(" + label + ").inV()[@class = " + type + "], inE(" + label + ").outV()[@class = " + type + "])) from " + this.element().getId();
            } else {
                query = "select expand(unionall(outE(" + label + ").inV(), inE(" + label + ").outV())) from " + this.element().getId();
            }
            final Set<Node> neighbors = new ArraySet<>();
            for (final Node node : this.graph.queryVertices(query)) {
                if (!this.equals(node)) {
                    neighbors.add(node);
                }
            }
            return Collections.unmodifiableCollection(neighbors);
        } catch (final Exception e) {
            throw new NormandraException("Unable to get edges for [" + this + "].", e);
        }
    }

    @Override
    public Iterable<Node> expand(final int depth) throws NormandraException {
        try (final Transaction tx = this.graph.beginTransaction()) {
            final Set<Node> nodes = new HashSet<>();
            for (final Node node : this.graph.queryVertices("traverse both() from " + this.element().getId() + " maxdepth " + depth)) {
                if (!this.equals(node)) {
                    nodes.add(node);
                }
            }
            return Collections.unmodifiableCollection(nodes);
        } catch (final Exception e) {
            throw new NormandraException("Unable to expand nodes from [" + this + "].", e);
        }
    }

    @Override
    public <E, N> Iterable<Node<N>> expandByType(final int depth, final Class<E> type) throws NormandraException {
        if (null == type) {
            return Collections.emptyList();
        }

        return this.expandByTypes(depth, type);
    }

    @Override
    public <E, N> Iterable<Node<N>> expandByTypes(final int depth, final Iterable<Class<E>> types) throws NormandraException {
        if (null == types) {
            return Collections.emptyList();
        }

        final Set<String> nodeTypes = new ArraySet<>();
        for (final Class<?> type : types) {
            final EntityMeta meta = this.graph.getMeta().getEdgeMeta(type);
            if (meta != null) {
                nodeTypes.add(meta.getTable());
            }
        }
        if (nodeTypes.isEmpty()) {
            return Collections.emptyList();
        }

        try (final Transaction tx = this.graph.beginTransaction()) {
            final List<String> quotedTypes = nodeTypes.stream().map((x) -> "'" + x + "'").collect(Collectors.toList());
            final String typeList = StringUtils.join(quotedTypes, ",");
            final Set<Node<N>> nodes = new HashSet<>();
            for (final Node node : this.graph.queryVertices("traverse both(" + typeList + ") from " + this.element().getId() + " maxdepth " + depth)) {
                if (!this.equals(node)) {
                    nodes.add(node);
                }
            }
            return Collections.unmodifiableCollection(nodes);
        } catch (final Exception e) {
            throw new NormandraException("Unable to expand nodes from [" + this + "] with types " + types + ".", e);
        }
    }

    @Override
    public <E> Iterable<Edge<E>> getEdges() throws NormandraException {
        try (final Transaction tx = this.graph.beginTransaction()) {
            return this.graph.queryEdges("select expand(bothE()) from " + this.element().getId());
        } catch (final Exception e) {
            throw new NormandraException("Unable to get edges for [" + this + "].", e);
        }
    }

    @Override
    public <E> Iterable<Edge<E>> getEdgesByType(final Class<E> type) throws NormandraException {
        if (null == type) {
            return Collections.emptyList();
        }

        final EntityMeta meta = this.graph.getMeta().getEdgeMeta(type);
        if (null == meta) {
            return Collections.emptyList();
        }

        return this.getEdgesByLabels(Collections.singleton(meta.getTable()));
    }

    @Override
    public <E> Iterable<Edge<E>> getEdgesByTypes(final Iterable<Class<E>> types) throws NormandraException {
        final Set<String> labels = new ArraySet<>();
        for (final Class<?> type : types) {
            final EntityMeta meta = this.graph.getMeta().getEdgeMeta(type);
            if (meta != null) {
                labels.add(meta.getTable());
            }
        }

        if (labels.isEmpty()) {
            return Collections.emptyList();
        }

        return getEdgesByLabels(labels);
    }

    private <E> Iterable<Edge<E>> getEdgesByLabels(final Iterable<String> labels) throws NormandraException {
        if (null == labels) {
            return Collections.emptyList();
        }

        final Set<String> set = new ArraySet<>();
        for (final String label : labels) {
            if (label != null) {
                set.add(label);
            }
        }

        final List<String> filtered = set.stream()
                .map(String::trim)
                .filter((x) -> !x.isEmpty())
                .map((x -> "'" + x + "'"))
                .collect(Collectors.toList());
        if (set.isEmpty()) {
            return Collections.emptyList();
        }

        try (final Transaction tx = this.graph.beginTransaction()) {
            return this.graph.queryEdges("select expand(bothE(" + StringUtils.join(filtered, ",") + ")) from " + this.element().getId());
        } catch (final Exception e) {
            throw new NormandraException("Unable to get edges of [" + this + "] by labels " + set + ".", e);
        }
    }

    @Override
    public void updateEntity(final T instance) throws NormandraException {
        // get entity context
        final Class<?> clazz = instance.getClass();
        final EntityMeta meta = this.graph.getMeta().getNodeMeta(clazz);
        if (null == meta) {
            throw new IllegalArgumentException("Unable to get meta for node [" + this + "].");
        }

        try (final Transaction tx = this.graph.beginTransaction()) {
            // update model
            final PropertyModel model = this.graph.buildModel(meta, this.vertex);
            final GraphDataHandler handler = new GraphDataHandler(model);
            new EntityPersistence(this.graph).save(meta, instance, handler);
            tx.success();
        } catch (final Exception e) {
            throw new NormandraException("Unable to update node [" + instance + "].", e);
        }

        // update instance
        final Object key = meta.getId().fromEntity(instance);
        this.data = new StaticEntityReference<>(instance);
        this.graph.cache().put(meta, key, this);
    }

    @Override
    public T getEntity() throws NormandraException {
        return this.data.getInstance();
    }

    @Override
    public boolean equals(Object o) {
        if (this == o) return true;
        if (o == null || getClass() != o.getClass()) return false;

        OrientNode<?> that = (OrientNode<?>) o;

        if (graph != null ? !graph.equals(that.graph) : that.graph != null) return false;
        if (meta != null ? !meta.equals(that.meta) : that.meta != null) return false;
        return vertex != null ? vertex.equals(that.vertex) : that.vertex == null;
    }

    @Override
    public int hashCode() {
        int result = graph != null ? graph.hashCode() : 0;
        result = 31 * result + (meta != null ? meta.hashCode() : 0);
        result = 31 * result + (vertex != null ? vertex.hashCode() : 0);
        return result;
    }
}
