package org.normandra.orientdb.data;

import com.orientechnologies.orient.core.db.record.OIdentifiable;
import com.orientechnologies.orient.core.db.record.ORecordLazyList;
import com.orientechnologies.orient.core.db.record.ORecordLazySet;
import com.orientechnologies.orient.core.metadata.schema.OType;
import com.orientechnologies.orient.core.record.impl.ODocument;
import org.normandra.meta.*;
import org.normandra.property.EmptyPropertyFilter;
import org.normandra.property.PropertyFilter;
import org.normandra.util.ArraySet;
import org.normandra.util.DataUtils;

import java.io.Serializable;
import java.math.BigDecimal;
import java.util.*;
import java.util.stream.Collectors;

/**
 * collection of common orient utilities
 * <p>
 * Date: 5/15/14
 */
public class OrientUtils {
    public static String url(final String url, final String database) {
        if (null == url) {
            throw new IllegalArgumentException();
        }
        final String lowercase = url.toLowerCase();
        if (lowercase.startsWith("plocal:") || lowercase.startsWith("local:") || lowercase.startsWith("embedded:") || lowercase.startsWith("memory:")) {
            return url;
        } else {
            return url + "/" + database;
        }
    }

    public static Object unpackValue(final ODocument document, final ColumnMeta column) {
        if (null == document || null == column) {
            return null;
        }

        final String name = column.getName();
        final Object raw = document.field(name);
        if (null == raw) {
            return null;
        }

        return OrientUtils.unpackRaw(column, raw);
    }

    public static Object unpackKey(final EntityMeta meta, final ODocument document) {
        if (null == meta || null == document) {
            return null;
        }

        final Map<ColumnMeta, Object> data = new LinkedHashMap<>();
        for (final ColumnMeta column : meta.getPrimaryKeys()) {
            final Object value = unpackValue(document, column);
            if (value != null) {
                data.put(column, value);
            }
        }
        return meta.getId().toKey(data);
    }

    public static Map<ColumnMeta, Object> unpackValues(final EntityMeta meta, final ODocument document) {
        if (null == meta || null == document) {
            return Collections.emptyMap();
        }

        final Map<ColumnMeta, Object> data = new LinkedHashMap<>();
        for (final ColumnMeta column : meta.getColumns()) {
            final String name = column.getName();
            if (document.containsField(name)) {
                final Object raw = document.field(name);
                final Object value = raw != null ? OrientUtils.unpackRaw(column, raw) : null;
                data.put(column, value);
            }
        }
        return Collections.unmodifiableMap(data);
    }

    public static Map<ColumnMeta, Object> unpackValues(final EntityMeta meta, final Map<ColumnMeta, Object> properties) {
        if (null == meta || null == properties || properties.isEmpty()) {
            return Collections.emptyMap();
        }

        final Map<ColumnMeta, Object> data = new LinkedHashMap<>();
        for (final Map.Entry<ColumnMeta, Object> entry : properties.entrySet()) {
            final ColumnMeta column = entry.getKey();
            final Object raw = entry.getValue();
            final Object value = raw != null ? OrientUtils.unpackRaw(column, raw) : null;
            data.put(column, value);
        }
        return Collections.unmodifiableMap(data);
    }

    private static Object unpackRaw(final ColumnMeta column, final Object value) {
        if (null == column || null == value) {
            return null;
        }

        if (value instanceof ORecordLazySet) {
            return value;
        }
        if (value instanceof ORecordLazyList) {
            return value;
        }

        final Class<?> clazz = column.getType();
        if (column.isCollection() && value instanceof Collection) {
            final Collection list = (Collection) value;
            final Class<?> generic;
            if (column instanceof EmbeddedCollectionMeta) {
                generic = ((EmbeddedCollectionMeta) column).getGeneric();
            } else if (column instanceof JoinCollectionMeta) {
                generic = ((JoinCollectionMeta) column).getEntity().getPrimaryKey().getType();
            } else {
                generic = column.getType();
            }
            final Collection packed;
            if (value instanceof List) {
                packed = new ArrayList<>(list.size());
            } else {
                packed = new ArraySet<>(list.size());
            }
            for (final Object item : list) {
                final Object pack = unpackPrimitive(column, generic, item);
                if (pack != null) {
                    packed.add(pack);
                }
            }
            return packed;
        }

        return unpackPrimitive(column, clazz, value);
    }

    public static Object[] packValuesAsArray(final Map<ColumnMeta, Object> data, final EntityMeta meta) {
        return packValuesAsArray(data, meta, EmptyPropertyFilter.getInstance());
    }

    public static Object[] packValuesAsArray(final Map<ColumnMeta, Object> data, final EntityMeta meta, final PropertyFilter filter) {
        if (null == data || data.isEmpty()) {
            return new Object[0];
        }

        final Map<String, Object> fields = new HashMap<>(data.size());
        for (final Map.Entry<ColumnMeta, Object> entry : data.entrySet()) {
            final ColumnMeta column = entry.getKey();
            if (filter.accept(meta, column)) {
                final Object packed = OrientUtils.packValue(column, entry.getValue());
                if (packed != null) {
                    final String name = column.getName();
                    fields.put(name, packed);
                }
            }
        }

        if (fields.isEmpty()) {
            return new Object[0];
        }

        final List<Object> list = new ArrayList<>(fields.size() * 2);
        for (final Map.Entry<String, Object> entry : fields.entrySet()) {
            list.add(entry.getKey());
            list.add(entry.getValue());
        }
        return list.stream().toArray();
    }

    public static Object packValue(final ColumnMeta column, final Object value) {
        if (null == column || null == value) {
            return null;
        }

        if (value instanceof ORecordLazySet) {
            return value;
        }
        if (value instanceof ORecordLazyList) {
            return value;
        }

        final Class<?> clazz = column.getType();
        if (column.isCollection() && value instanceof Collection) {
            final Collection list = (Collection) value;
            final List<Object> packed = new ArrayList<>(list.size());
            for (final Object item : list) {
                final Object pack;
                if (column.isJson()) {
                    pack = DataUtils.objectToJson(item);
                } else {
                    pack = packPrimitive(item);
                }
                packed.add(pack);
            }
            if (List.class.isAssignableFrom(clazz)) {
                return new ArrayList<>(packed);
            } else if (Collection.class.isAssignableFrom(clazz)) {
                return new ArraySet<>(packed);
            }
        }

        if (column.isJson()) {
            return DataUtils.objectToJson(value);
        }

        return packPrimitive(value);
    }

    private static Object unpackPrimitive(final ColumnMeta column, final Class<?> clazz, final Object value) {
        if (null == value) {
            return null;
        }
        if (value instanceof OIdentifiable) {
            return ((OIdentifiable) value).getIdentity();
        }
        if (UUID.class.equals(clazz)) {
            return DataUtils.stringToUUID(value.toString());
        }
        if (Date.class.equals(clazz)) {
            return value;
        }
        if (Enum.class.isAssignableFrom(clazz)) {
            return Enum.valueOf((Class) clazz, value.toString());
        }
        if (column.isJson() && value instanceof CharSequence) {
            return DataUtils.jsonToObject(value.toString(), clazz);
        }
        if (String.class.equals(clazz)) {
            return value;
        }
        if (Number.class.isAssignableFrom(clazz)) {
            return value;
        }
        if (byte[].class.equals(clazz)) {
            return value;
        }
        if (Boolean.class.equals(clazz)) {
            return value;
        }
        if (Serializable.class.isAssignableFrom(clazz) && byte[].class.isInstance(value)) {
            final Class serialized = clazz;
            return DataUtils.bytesToObject(serialized, (byte[]) value);
        }
        return value;
    }

    public static Object packPrimitive(final Object value) {
        if (null == value) {
            return null;
        }
        if (value instanceof OIdentifiable) {
            return ((OIdentifiable) value).getIdentity();
        }
        if (value instanceof Date) {
            return value;
        }
        if (value instanceof Enum) {
            return ((Enum) value).name();
        }
        if (value instanceof String) {
            return value;
        }
        if (value instanceof Boolean) {
            return value;
        }
        if (value instanceof UUID) {
            return DataUtils.uuidToString((UUID) value);
        }
        if (value instanceof Number) {
            final Class<?> clazz = value.getClass();
            final Number number = (Number) value;
            if (Long.class.equals(clazz)) {
                return number.longValue();
            } else if (Integer.class.equals(clazz)) {
                return number.intValue();
            } else if (Short.class.equals(clazz)) {
                return number.shortValue();
            } else if (Float.class.equals(clazz)) {
                return number.floatValue();
            } else if (Double.class.equals(clazz)) {
                return number.doubleValue();
            } else {
                throw new IllegalArgumentException("Unexpected numeric type [" + clazz + "] with value [" + value + "].");
            }
        }
        if (value instanceof Collection) {
            final Collection packed = (Collection) value;
            return packed.stream()
                    .map((x) -> packPrimitive(x))
                    .collect(Collectors.toList());
        }
        if (byte[].class.equals(value.getClass())) {
            return value;
        }
        if (value instanceof Serializable) {
            return DataUtils.objectToBytes((Serializable) value);
        }

        throw new IllegalArgumentException("Unexpected value type [" + value.getClass() + "] with value [" + value + "].");
    }

    public static OType columnType(final ColumnMeta column) {
        if (null == column) {
            return null;
        }

        if (column instanceof MappedColumnMeta) {
            return null;
        }

        final Class<?> clazz = column.getType();

        // handle collections
        if (column instanceof EmbeddedCollectionMeta) {
            if (List.class.isAssignableFrom(clazz)) {
                return OType.EMBEDDEDLIST;
            } else {
                return OType.EMBEDDEDSET;
            }
        } else if (column instanceof JoinColumnMeta) {
            return OType.LINK;
        } else if (column instanceof JoinCollectionMeta) {
            if (column.isEmbedded()) {
                if (List.class.isAssignableFrom(clazz)) {
                    return OType.LINKLIST;
                } else {
                    return OType.LINKSET;
                }
            } else {
                return OType.LINK;
            }
        } else {
            return primitiveType(column, clazz);
        }
    }

    private static OType primitiveType(final ColumnMeta column, final Class<?> clazz) {
        // handle regular values
        if (String.class.equals(clazz) || column.isJson()) {
            return OType.STRING;
        }
        if (Enum.class.isAssignableFrom(clazz)) {
            return OType.STRING;
        }
        if (long.class.equals(clazz) || Long.class.equals(clazz)) {
            return OType.LONG;
        }
        if (int.class.equals(clazz) || Integer.class.equals(clazz)) {
            return OType.INTEGER;
        }
        if (short.class.equals(clazz) || Short.class.equals(clazz)) {
            return OType.SHORT;
        }
        if (double.class.equals(clazz) || Double.class.equals(clazz)) {
            return OType.DOUBLE;
        }
        if (float.class.equals(clazz) || Float.class.equals(clazz)) {
            return OType.FLOAT;
        }
        if (BigDecimal.class.equals(clazz)) {
            return OType.DECIMAL;
        }
        if (boolean.class.equals(clazz) || Boolean.class.equals(clazz)) {
            return OType.BOOLEAN;
        }
        if (Date.class.equals(clazz)) {
            return OType.DATETIME;
        }
        if (UUID.class.equals(clazz)) {
            // orientdb stored them as base64 internally, so not losing anything storing as string
            // see http://orientdb.com/docs/3.0.x/java/Binary-Data.html
            return OType.STRING;
        }
        return OType.BINARY;
    }

    static String keyIndex(final EntityMeta table) {
        if (null == table) {
            return null;
        }

        final Collection<ColumnMeta> keys = table.getPrimaryKeys();
        if (keys.size() == 1) {
            final ColumnMeta key = keys.iterator().next();
            return table.getTable() + "." + key.getName();
        } else {
            return table.getTable() + ".key";
        }
    }

    private OrientUtils() {
    }
}
